export interface ProductHistory {
  date: string;
  time: string;
  buy: number;
  sell: number;
}

export interface Product {
  id: string;
  name: string;
  currentBuy: number;
  currentSell: number;
  buyChange: number;
  sellChange: number;
  history: ProductHistory[];
}

function formatProductName(name: string): string {
  if (!name) return name;
  let formatted = name.toLowerCase();
  formatted = formatted.charAt(0).toUpperCase() + formatted.slice(1);
  formatted = formatted.replace(/phú quý/g, 'Phú Quý');
  return formatted;
}

export function parseData(): Product[] {
  const lines = rawTsv.trim().split('\n');
  const headers = lines[0].split('\t');
  
  const productNames = [];
  for (let i = 2; i < headers.length; i += 2) {
    const name = headers[i].replace(' - Mua', '');
    productNames.push(formatProductName(name));
  }

  const products: Product[] = productNames.map((name, i) => ({
    id: `product-${i}`,
    name,
    currentBuy: 0,
    currentSell: 0,
    buyChange: 0,
    sellChange: 0,
    history: []
  }));

  for (let i = 1; i < lines.length; i++) {
    const cells = lines[i].split('\t');
    const date = cells[0];
    const time = cells[1];
    
    for (let j = 0; j < productNames.length; j++) {
      const buy = parseInt(cells[2 + j * 2], 10);
      const sell = parseInt(cells[2 + j * 2 + 1], 10);
      
      products[j].history.push({
        date,
        time,
        buy,
        sell
      });
    }
  }

  for (const product of products) {
    const latest = product.history[product.history.length - 1];
    const previous = product.history[product.history.length - 2];
    
    product.currentBuy = latest.buy;
    product.currentSell = latest.sell;
    
    if (previous) {
      product.buyChange = latest.buy - previous.buy;
      product.sellChange = latest.sell - previous.sell;
    }
  }

  return products;
}
